-- Create database

CREATE DATABASE TorneoTenis;

-- Create "Coach" Table 

CREATE TABLE Coach
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR (30),
    last_name VARCHAR (30),
    PRIMARY KEY (id)
)
;

-- Create "Player" Table

CREATE TABLE Player
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR (30),
    last_name VARCHAR (30),
    age INT(2), 
    PRIMARY KEY (id)
)
;

-- Create "Country" Table

CREATE TABLE Country
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR (30),
    PRIMARY KEY (id)
)
;

-- Create "Round" Table

CREATE TABLE Round
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR (30),
    prize_winner INT,
    prize_loser INT,
    PRIMARY KEY (id)
)
;

-- Create "Category" Table

CREATE TABLE Category
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR (30),
    PRIMARY KEY (id)
)
;

-- Create "Referee" Table

CREATE TABLE Referee
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR (30),
    last_name VARCHAR (30),
    PRIMARY KEY (id)
)
;

-- Create "Tournament" Table

CREATE TABLE Tournament
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR (30),
    year INT (4),
    id_category INT,
    PRIMARY KEY (id),
    FOREIGN KEY (id_category) REFERENCES Category (id)
)
;

-- Create "City" Table

CREATE TABLE City
(
    id INT NOT NULL AUTO_INCREMENT,
    name VARCHAR (30),
    id_country INT,
    PRIMARY KEY (id),
    FOREIGN KEY (id_country) REFERENCES Country (id)
)
;

-- Create "Tournament_Cities" Table

CREATE TABLE Tournament_Cities
(
    id INT NOT NULL AUTO_INCREMENT,
    id_tournament INT,
    id_city INT,
    PRIMARY KEY (id),
    FOREIGN KEY (id_tournament) REFERENCES Tournament (id),
    FOREIGN KEY (id_city) REFERENCES City (id)
)
;

-- Create "Country_Players" Table

CREATE TABLE Country_Players
(
    id INT NOT NULL AUTO_INCREMENT,
    id_player INT,
    id_country INT,
    PRIMARY KEY (id),
    FOREIGN KEY (id_player) REFERENCES Player (id),
    FOREIGN KEY (id_country) REFERENCES Country (id)
)
;

-- Create "Coach_Players" Table

CREATE TABLE Coach_Players
(
    id INT NOT NULL AUTO_INCREMENT,
    id_coach INT,
    id_player INT,
    start_date DATE,
    end_date DATE, 
    PRIMARY KEY (id),
    FOREIGN KEY (id_player) REFERENCES Player (id),
    FOREIGN KEY (id_coach) REFERENCES Coach (id)
)
;

-- Create "Match" Table

CREATE TABLE Game
(
    id INT NOT NULL AUTO_INCREMENT,
    date DATE,
    id_round INT,
    id_tournament INT,
    PRIMARY KEY (id),
    FOREIGN KEY (id_round) REFERENCES Round (id),
    FOREIGN KEY (id_tournament) REFERENCES Tournament (id)
)
;

-- Create "Scores" Table

CREATE TABLE Scores
(
    id INT NOT NULL AUTO_INCREMENT,
    date DATE,
    id_game INT,
    id_player INT,
    PRIMARY KEY (id),
    FOREIGN KEY (id_game) REFERENCES Game (id),
    FOREIGN KEY (id_player) REFERENCES Player (id)
)
;

-- Create "Tournament_Players" Table

CREATE TABLE Tournament_Players
(
    id INT NOT NULL AUTO_INCREMENT,
    id_tournament INT,
    id_player INT,
    PRIMARY KEY (id),
    FOREIGN KEY (id_tournament) REFERENCES Tournament (id),
    FOREIGN KEY (id_player) REFERENCES Player (id)
)
;

-- Create "Referee_Games" Table

CREATE TABLE Referee_Games
(
    id INT NOT NULL AUTO_INCREMENT,
    id_referee INT,
    id_game INT,
    PRIMARY KEY (id),
    FOREIGN KEY (id_referee) REFERENCES Referee (id),
    FOREIGN KEY (id_game) REFERENCES Game (id)
)
;

